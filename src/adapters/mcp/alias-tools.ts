// ─── Typed request builders (DELTA-006) ──────────────────────────────────────
// Pure functions that read only the declared schema fields. They replace the
// structural `validatedInput as { ... }` casts previously inlined in the
// `buildAliasTools` handlers.

function isMcpToolResult(value: unknown): value is McpToolResult {
  return (
    typeof value === "object" &&
    value !== null &&
    "content" in value &&
    "isError" in value &&
    (value as { isError: unknown }).isError === true
  );
}

export { isMcpToolResult };

/**
 * Build a typed request for `cleanup_access_operation`. Reads only declared
 * fields; unknown inputs are dropped at the type boundary, not propagated.
 */
export interface CleanupRequest {
  operationId: string;
  accessPath: string;
  force?: boolean;
  projectId?: string;
  contextId?: string;
  backendPath?: string;
  destinationRoot?: string;
  projectRoot?: string;
  timeoutMs?: number;
  strictContext?: boolean;
  expectedAccessPath?: string;
  expectedProjectRoot?: string;
  expectedDestinationRoot?: string;
}

export function buildCleanupRequest(input: unknown): CleanupRequest | McpToolResult {
  if (!isRecord(input)) return invalidInput("cleanup_access_operation requires an object input.");
  return {
    operationId: typeof input.operationId === "string" ? input.operationId : "",
    accessPath: typeof input.accessPath === "string" ? input.accessPath : "",
    force: input.force === true ? true : undefined,
    projectId: typeof input.projectId === "string" ? input.projectId : undefined,
    contextId: typeof input.contextId === "string" ? input.contextId : undefined,
    backendPath: typeof input.backendPath === "string" ? input.backendPath : undefined,
    destinationRoot: typeof input.destinationRoot === "string" ? input.destinationRoot : undefined,
    projectRoot: typeof input.projectRoot === "string" ? input.projectRoot : undefined,
    timeoutMs: typeof input.timeoutMs === "number" ? input.timeoutMs : undefined,
    strictContext: input.strictContext === true ? true : undefined,
    expectedAccessPath:
      typeof input.expectedAccessPath === "string" ? input.expectedAccessPath : undefined,
    expectedProjectRoot:
      typeof input.expectedProjectRoot === "string" ? input.expectedProjectRoot : undefined,
    expectedDestinationRoot:
      typeof input.expectedDestinationRoot === "string" ? input.expectedDestinationRoot : undefined,
  };
}

/**
 * Build a typed request for `run_vba`. Parses `argsJson` via the shared helper
 * and returns an `McpToolResult` (not throws) when argsJson is malformed.
 */
export function buildRunVbaRequest(input: unknown): AccessVbaRequest | McpToolResult {
  if (!isRecord(input)) return invalidInput("run_vba requires an object input.");
  const parsedArgs = parseMcpArgsJson(
    typeof input.argsJson === "string" ? input.argsJson : undefined,
  );
  if (!parsedArgs.ok) return invalidInput(parsedArgs.message);

  return {
    moduleName: "",
    procedureName: typeof input.procedureName === "string" ? input.procedureName : "",
    arguments: parsedArgs.value,
    projectId: typeof input.projectId === "string" ? input.projectId : undefined,
    contextId: typeof input.contextId === "string" ? input.contextId : undefined,
    accessPath: typeof input.accessPath === "string" ? input.accessPath : undefined,
    backendPath: typeof input.backendPath === "string" ? input.backendPath : undefined,
    destinationRoot: typeof input.destinationRoot === "string" ? input.destinationRoot : undefined,
    projectRoot: typeof input.projectRoot === "string" ? input.projectRoot : undefined,
    timeoutMs: typeof input.timeoutMs === "number" ? input.timeoutMs : undefined,
    strictContext: input.strictContext === true ? true : undefined,
    expectedAccessPath:
      typeof input.expectedAccessPath === "string" ? input.expectedAccessPath : undefined,
    expectedProjectRoot:
      typeof input.expectedProjectRoot === "string" ? input.expectedProjectRoot : undefined,
    expectedDestinationRoot:
      typeof input.expectedDestinationRoot === "string" ? input.expectedDestinationRoot : undefined,
  };
}

/**
 * Build a typed request for `query_sql`. Rejects empty / whitespace-only
 * `sql` or `query` (DELTA-010) before constructing the request.
 */
export function buildQuerySqlRequest(input: unknown): AccessQueryRequest | McpToolResult {
  if (!isRecord(input)) return invalidInput("query_sql requires an object input.");

  // DELTA-010 — reject empty/whitespace-only sql/query (was: `?? ""`).
  const rawSql = typeof input.sql === "string" ? input.sql : undefined;
  const rawQuery = typeof input.query === "string" ? input.query : undefined;
  const sql = rawSql ?? rawQuery ?? "";
  if (!sql.trim()) {
    return invalidInput("query_sql requires sql or query.");
  }

  return {
    sql,
    mode: "read",
    backendPath: typeof input.backendPath === "string" ? input.backendPath : undefined,
    databasePath:
      typeof input.databasePath === "string"
        ? input.databasePath
        : typeof input.sourcePath === "string"
          ? input.sourcePath
          : undefined,
    projectId: typeof input.projectId === "string" ? input.projectId : undefined,
    contextId: typeof input.contextId === "string" ? input.contextId : undefined,
    accessPath: typeof input.accessPath === "string" ? input.accessPath : undefined,
    destinationRoot: typeof input.destinationRoot === "string" ? input.destinationRoot : undefined,
    projectRoot: typeof input.projectRoot === "string" ? input.projectRoot : undefined,
    timeoutMs: typeof input.timeoutMs === "number" ? input.timeoutMs : undefined,
    strictContext: input.strictContext === true ? true : undefined,
    expectedAccessPath:
      typeof input.expectedAccessPath === "string" ? input.expectedAccessPath : undefined,
    expectedProjectRoot:
      typeof input.expectedProjectRoot === "string" ? input.expectedProjectRoot : undefined,
    expectedDestinationRoot:
      typeof input.expectedDestinationRoot === "string" ? input.expectedDestinationRoot : undefined,
  };
}

function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null && !Array.isArray(value);
}

// ─── Alias tools ──────────────────────────────────────────────────────────────

export function buildAliasTools(
  services: DysflowMcpServices,
  writesEnabled: boolean,
  writeAccessResolver: McpWriteAccessResolver | undefined,
  allowedProcedures: readonly string[] | undefined,
): DysflowMcpTool[] {
  const cleanupSchema = mcpSchemaFor("cleanup_access_operation");
  const runVbaSchema = mcpSchemaFor("run_vba");
  const querySqlSchema = mcpSchemaFor("query_sql");
  const execSqlSchema = mcpSchemaFor("exec_sql");
  const runScriptSchema = mcpSchemaFor("run_script");
  const createTableSchema = mcpSchemaFor("create_table");
  const dropTableSchema = mcpSchemaFor("drop_table");
  const seedFixtureSchema = mcpSchemaFor("seed_fixture");
  const teardownFixtureSchema = mcpSchemaFor("teardown_fixture");

  return [
    {
      name: "list_access_operations",
      description: TOOL_DESCRIPTIONS.list_access_operations,
      inputSchema: NO_INPUT_SCHEMA,
      handler: async () => handleMcpAccessOperationsList(services),
    },
    {
      name: "cleanup_access_operation",
      description: TOOL_DESCRIPTIONS.cleanup_access_operation,
      inputSchema: cleanupSchema,
      handler: async (input) =>
        handleMcpAccessCleanup(
          input,
          cleanupSchema,
          services,
          writesEnabled,
          writeAccessResolver,
          (validatedInput) => {
            const request = buildCleanupRequest(validatedInput);
            if (isMcpToolResult(request)) return request;
            return {
              operationId: request.operationId,
              accessPath: request.accessPath,
              force: request.force,
              projectId: request.projectId,
              contextId: request.contextId,
              backendPath: request.backendPath,
              destinationRoot: request.destinationRoot,
              projectRoot: request.projectRoot,
              timeoutMs: request.timeoutMs,
              strictContext: request.strictContext,
              expectedAccessPath: request.expectedAccessPath,
              expectedProjectRoot: request.expectedProjectRoot,
              expectedDestinationRoot: request.expectedDestinationRoot,
            };
          },
        ),
    },
    {
      name: "run_vba",
      description: TOOL_DESCRIPTIONS.run_vba,
      inputSchema: runVbaSchema,
      handler: async (input) =>
        handleMcpVbaExecute(input, runVbaSchema, services, allowedProcedures, (validatedInput) =>
          buildRunVbaRequest(validatedInput),
        ),
    },
    {
      name: "query_sql",
      description: TOOL_DESCRIPTIONS.query_sql,
      inputSchema: querySqlSchema,
      handler: async (input) => {
        // Run schema validation FIRST so type errors surface as schema errors
        // ("sql must be a string", etc.) before the typed builder runs. Then
        // apply the DELTA-010 empty-sql guard via the typed builder.
        const validation = validateInput(input, querySqlSchema);
        if (validation !== undefined) return invalidInput(validation);

        // DELTA-010 — empty sql/query is rejected by the builder BEFORE we
        // enter handleMcpQueryExecute (whose buildRequest signature returns
        // AccessQueryRequest only).
        const precheck = buildQuerySqlRequest(input);
        if (isMcpToolResult(precheck)) return precheck;
        return handleMcpQueryExecute(
          input,
          querySqlSchema,
          services,
          writesEnabled,
          writeAccessResolver,
          (validatedInput) => {
            const request = buildQuerySqlRequest(validatedInput);
            // Validation passed for the raw input; should not return McpToolResult
            // here, but assert defensively to keep the typed contract.
            if (isMcpToolResult(request)) {
              throw new Error("buildQuerySqlRequest returned McpToolResult after precheck passed");
            }
            return request;
          },
        );
      },
    },
    {
      name: "exec_sql",
      description: TOOL_DESCRIPTIONS.exec_sql,
      inputSchema: execSqlSchema,
      handler: async (input) =>
        handleValidatedMcpWrite(input, execSqlSchema, writesEnabled, writeAccessResolver, () =>
          services.queryService.execute(
            buildWriteFixtureRequest(MCP_TOOL_QUERY_ACTIONS.exec_sql, input),
          ),
        ),
    },
    {
      name: "run_script",
      description: TOOL_DESCRIPTIONS.run_script,
      inputSchema: runScriptSchema,
      handler: async (input) =>
        handleValidatedMcpWrite(input, runScriptSchema, writesEnabled, writeAccessResolver, () =>
          services.queryService.execute(
            buildWriteFixtureRequest(MCP_TOOL_QUERY_ACTIONS.run_script, input),
          ),
        ),
    },
    {
      name: "create_table",
      description: TOOL_DESCRIPTIONS.create_table,
      inputSchema: createTableSchema,
      handler: async (input) =>
        handleValidatedMcpWrite(input, createTableSchema, writesEnabled, writeAccessResolver, () =>
          services.queryService.execute(
            buildWriteFixtureRequest(MCP_TOOL_QUERY_ACTIONS.create_table, input),
          ),
        ),
    },
    {
      name: "drop_table",
      description: TOOL_DESCRIPTIONS.drop_table,
      inputSchema: dropTableSchema,
      handler: async (input) =>
        handleValidatedMcpWrite(input, dropTableSchema, writesEnabled, writeAccessResolver, () =>
          services.queryService.execute(
            buildWriteFixtureRequest(MCP_TOOL_QUERY_ACTIONS.drop_table, input),
          ),
        ),
    },
    {
      name: "seed_fixture",
      description: TOOL_DESCRIPTIONS.seed_fixture,
      inputSchema: seedFixtureSchema,
      handler: async (input) =>
        handleValidatedMcpWrite(input, seedFixtureSchema, writesEnabled, writeAccessResolver, () =>
          services.queryService.execute(
            buildWriteFixtureRequest(MCP_TOOL_QUERY_ACTIONS.seed_fixture, input),
          ),
        ),
    },
    {
      name: "teardown_fixture",
      description: TOOL_DESCRIPTIONS.teardown_fixture,
      inputSchema: teardownFixtureSchema,
      handler: async (input) =>
        handleValidatedMcpWrite(
          input,
          teardownFixtureSchema,
          writesEnabled,
          writeAccessResolver,
          () =>
            services.queryService.execute(
              buildWriteFixtureRequest(MCP_TOOL_QUERY_ACTIONS.teardown_fixture, input),
            ),
        ),
    },
  ];
}
